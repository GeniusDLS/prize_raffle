// Пресет: Стрес-тест (1000 учасників)
// Учасники генеруються програмно: 1000 унікальних імен, сума ваги = 4000
// Розподіл ваги: тир 1 (×1, 750 ос.), тир 2 (×5, 170 ос.), тир 3 (×20, 50 ос.),
//                тир 4 (×40, 25 ос.), тир 5 (×80, 5 ос.)
window.DataManager.TEST_DATA_PRESETS.stress_1000 = (() => {
    const FEMALE_NAMES = [
        'Олена', 'Марія', 'Ірина', 'Тетяна', 'Наталя',
        'Оксана', 'Юлія', 'Людмила', 'Ганна', 'Катерина',
        'Валентина', 'Надія', 'Лариса', 'Світлана', 'Зоя',
        'Леся', 'Вікторія', 'Аліна', 'Дарина', 'Поліна',
    ];
    const MALE_NAMES = [
        'Андрій', 'Дмитро', 'Іван', 'Олексій', 'Сергій',
        'Василь', 'Микола', 'Павло', 'Олег', 'Максим',
        'Роман', 'Тарас', 'Богдан', 'Юрій', 'Владислав',
        'Антон', 'Степан', 'Федір', 'Михайло', 'Євген',
    ];
    const LAST_NAMES = [
        'Коваленко', 'Мельник',    'Шевченко',    'Бондаренко', 'Ткаченко',
        'Кравченко', 'Лисенко',    'Мороз',       'Петренко',   'Савченко',
        'Гончаренко','Іваненко',   'Марченко',    'Харченко',   'Романенко',
        'Даниленко', 'Захаренко',  'Карпенко',    'Науменко',   'Олексієнко',
        'Павленко',  'Семененко',  'Тимченко',    'Цушко',      'Шаповаленко',
    ];
    const POSITIONS = [
        'Спеціаліст', 'Аналітик', 'Менеджер', 'Тімлід', 'Директор',
        'Розробник',  'Дизайнер', 'Консультант', 'Координатор', 'Адміністратор',
    ];
    const DIVISIONS = [
        'Відділ розробки',     'Відділ маркетингу',  'Фінансовий відділ',
        'HR відділ',           'Відділ продажів',    'IT підтримка',
        'Відділ аналітики',    'Відділ логістики',   'Юридичний відділ',
        'Бухгалтерія',         'Відділ якості',      'Відділ закупівель',
        'Виробничий відділ',   'Відділ PR',          'Відділ безпеки',
        'Адміністративний відділ', 'Відділ дизайну', 'Відділ стратегії',
        'Відділ навчання',     'Відділ інновацій',
    ];

    // Генерація 1000 унікальних імен: 500 жіночих (20×25) + 500 чоловічих (20×25)
    const nameEntries = [];
    for (let i = 0; i < FEMALE_NAMES.length; i++) {
        for (let j = 0; j < LAST_NAMES.length; j++) {
            nameEntries.push(`${LAST_NAMES[j]} ${FEMALE_NAMES[i]}`);
        }
    }
    for (let i = 0; i < MALE_NAMES.length; i++) {
        for (let j = 0; j < LAST_NAMES.length; j++) {
            nameEntries.push(`${LAST_NAMES[j]} ${MALE_NAMES[i]}`);
        }
    }

    // Розподіл ваги по тирах (сума = 4000)
    // Тир 1: індекси   0–749  → вага  1  (750 × 1  =  750)
    // Тир 2: індекси 750–919  → вага  5  (170 × 5  =  850)
    // Тир 3: індекси 920–969  → вага 20  ( 50 × 20 = 1000)
    // Тир 4: індекси 970–994  → вага 40  ( 25 × 40 = 1000)
    // Тир 5: індекси 995–999  → вага 80  (  5 × 80 =  400)
    const weightForIndex = (idx) => {
        if (idx < 750)  return 1;
        if (idx < 920)  return 5;
        if (idx < 970)  return 20;
        if (idx < 995)  return 40;
        return 80;
    };

    const participants = nameEntries.map((name, idx) => ({
        name,
        position: POSITIONS[idx % POSITIONS.length],
        division: DIVISIONS[idx % DIVISIONS.length],
        weight:   weightForIndex(idx),
    }));

    // Призи: 10 типів, чергування коротких/довгих назв — тест переносу в барабані
    // Загальна кількість: 1+2+2+1+1+3+5+5+2+3 = 25 призів
    const prizes = [
        { name: '🏆 Гран-прі',                                          count: 1 },
        { name: '📱 Смартфон Samsung Galaxy S24 Ultra 256GB',           count: 2 },
        { name: '💻 Ноутбук',                                           count: 2 },
        { name: '🎮 Ігрова консоль PlayStation 5 Slim + 2 геймпади',    count: 1 },
        { name: '☕ Кавомашина',                                        count: 1 },
        { name: '🎧 Навушники Sony WH-1000XM5 з активним шумодавом',    count: 3 },
        { name: '🎁 Сертифікат',                                        count: 5 },
        { name: '🍫 Шоколадний подарунковий набір преміум-класу',        count: 5 },
        { name: '🌺 Квіти',                                             count: 2 },
        { name: '🎒 Корпоративний рюкзак з вишивкою логотипу',          count: 3 },
    ];

    return {
        label: '🚀 Стрес-тест (1000)',
        description: '1000 учасників, 10 типів призів (25 шт.) — перевірка продуктивності',
        participants,
        prizes,
    };
})();
